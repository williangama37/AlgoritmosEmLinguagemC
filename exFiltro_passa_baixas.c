/*Bibliotecas*/
#include <stdio.h>
#include <stdlib.h>

/*---função principal---*/
int main()
{
    double resistor = 3.0E3,
           capacitor = 1.0E-6,
           pi = 3.14159265,
           frequencia;
    
    frequencia = 1/(2*pi*resistor*capacitor);
    printf("%.2fHz\n", frequencia);

    system("PAUSE");
    return 0;
} /*end main*/
